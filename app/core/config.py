"""Конфиг сервиса"""

from pydantic_settings import BaseSettings
from typing import Optional

class Settings(BaseSettings):
    DATABASE_URL: str
    REDIS_URL: Optional[str] = None
    SECRET_KEY: str
    PERIODIC_URL: str
    PERIODIC_INTERVAL: int = 30
    ALGORITHM: str = "HS256"
    ACCESS_TOKEN_EXPIRE_MINUTES: int = 30

    class Config:
        env_file = ".env"

settings = Settings()